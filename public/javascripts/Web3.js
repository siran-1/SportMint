function initializeWeb3() {
	web3 = new Web3(window.ethereum);
	// const networkId = await web3.eth.net.getId();
	// const chainId = await web3.eth.getChainId();
	initializeContract();
}

// initializing the contract
async function initializeContract() {
	sportMinterContract = new web3.eth.Contract(contractABI, contractAddress);
	const accounts = await web3.eth.getAccounts();
	const account = accounts[0];
}

// Mint function
window.mintNFT = async function (tokenid) {
	const accounts = await web3.eth.getAccounts();
	const account = accounts[0];

	sportMinterContract.methods.safeMint(account, tokenid).send({ from: account })
		.on('transactionHash', function (hash) {
			console.log('Transaction Hash:', hash);
		})
		.on('confirmation', function (confirmationNumber, receipt) {
			etherio();
		})
		.on('error', console.error);
}


function etherio() {
	fetch(apiendpoint + `?module=account&action=tokennfttx&contractaddress=${contractAddress}&page=1&offset=100&startblock=0&endblock=27025780&sort=asc&apikey=${etherscantoken}`)
		.then(response => response.json())
		.then(data => {
			const { result } = data;
			console.log(result);
			result.forEach(element => {
				const nft = document.getElementById(`${element.tokenID}`);
				if (nft) {
					nft.style.display = 'none'; 
				}
			});
		})
		.catch(error => {
			console.error('Error fetching data: ', error);
		});
}



